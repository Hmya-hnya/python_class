# 양의 정수 n을 입력 받아 n * n 크기의 행렬을 출력하는 프로그램 작성
# 이 때 행렬의 값은 1부터 시작하여 왼족에서 오른쪽, 위에서 아래 순서대로 채워 넣음:
# # 입력: 3
# 1 2 3
# 4 5 6
# 7 8 9

# 1. 입력 받은 값으로 실제 출력 범위를 정해야 함
# 2. 줄바꿈에 대한 처리 (나머지 연산자 사용: %)
# 3. 줄맞춤 신경써야 됨

n =  int(input("정수를 입력 하세요: "))
for i in range(1, n * n+1, 1):  #미만 개념이기 때문에 실재 원하는 출력 범위에서 +1 해야 함
    print(f"{i:5}",end="")
    if i % n == 0 : print()
